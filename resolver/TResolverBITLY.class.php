  class TResolverBITLY extends TResolver {
  // **************************************************************************
 
    // ------------------------------------------------------------------------
    /**
     *
     *
     * @param string $sName
     * @return TResolverBITLY
     */
    public function __construct($sName) {
    // ------------------------------------------------------------------------
      $this->SetName($sName);
 
      $this->SetAPI("http://api.bitly.com/v3/expand?apiKey=".BITLY_API_KEY."&login=".BITLY_LOGIN."&format=txt&domain=".substr($sName, 7)."&hash=");
    }
    // ------------------------------------------------------------------------
 
  }
